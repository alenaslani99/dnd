    {
        return [
            'product_id' => Product::factory(),
            'path' => '/assets/img/pexels-suhashanjar-36779951.webp',
            'alt_text' => fake()->optional()->words(3, true),
            'sort_order' => 0,
            'is_primary' => false,
        ];
    }

    public function primary(): static
    {
        return $this->state(fn (array $attributes) => [
            'is_primary' => true,
        ]);
    }
}
